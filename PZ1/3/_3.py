# Функція для перевірки, чи є слово паліндромом
def is_palindrome(word):
    # Приводимо слово до нижнього регістру та порівнюємо з оберненим
    return word.lower() == word[::-1].lower()

# Запит слова у користувача
text = input("Введи слово: ")

# Перевірка та виведення результату
if is_palindrome(text):
    print("Це паліндром!")
else:
    print("Це не паліндром!")
