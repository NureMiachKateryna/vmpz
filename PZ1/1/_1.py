# Функція для знаходження найменшого з трьох чисел
def find_minimum(a, b, c):
    # Знаходимо мінімальне значення
    minimum = min(a, b, c)
    print("Найменше значення:", minimum)

# Запит числа у користувача
a = float(input("Введи перше число: "))
b = float(input("Введи друге число: "))
c = float(input("Введи третє число: "))

# Виклик функції з введеними значеннями
find_minimum(a, b, c)
